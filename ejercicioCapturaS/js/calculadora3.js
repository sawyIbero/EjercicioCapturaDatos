// Función suma
function sumar() {
    var num1 = parseInt(prompt("Ingrese el primer número: "));
    var num2 = parseInt(prompt("Ingrese el segundo número: "));
    var resultado = num1 + num2;
    alert("El resultado de la suma es: " + resultado);
  }
  
